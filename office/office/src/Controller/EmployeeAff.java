                String add7=rs.getString("salary");
                bsaltxt.setText(add7);
                String add8=rs.getString("roll");
                rolltxt.setText(add8);
                String add9=rs.getString("address");
                addresstxt.setText(add9);
                String add10=rs.getString("phone");
