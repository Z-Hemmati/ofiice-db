        public void setCal(){
        Calendar cal = new GregorianCalendar();
        int year = cal.get(Calendar.YEAR);
        int month = cal.get(Calendar.MONTH);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        int hour = cal.get(Calendar.HOUR);
        int minute = cal.get(Calendar.MINUTE);
        int second = cal.get(Calendar.SECOND);
        dateLab.setText("Today's date : "+  day + " / " + (month + 1)+ " / "  + year);
        timelab.setText("Time : "+hour+" : "+minute+" : "+second);
       
        
    }
    
      private void table_update(){
          ResultSet rs;
        int c;
        try {

            rs= empServ.getInstance().select();
            ResultSetMetaData Rss=rs.getMetaData();
            c=Rss.getColumnCount();
            DefaultTableModel Df=(DefaultTableModel)deptab.getModel();
            Df.setRowCount(0);
            
            while(rs.next()){
                Vector v2=new Vector();
                
                for(int a=1;a<=c;a++){
                    v2.add(rs.getString("code"));
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("age"));
                    v2.add(rs.getString("fname"));
                    v2.add(rs.getString("education"));
                    v2.add(rs.getString("salary"));
                    v2.add(rs.getString("roll"));
                    v2.add(rs.getString("address"));
                    v2.add(rs.getString("phone"));
                    
                }
                
                Df.addRow(v2);   
            }
           
            }catch (SQLException ex) {
            Logger.getLogger(Document.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception e) {
            e.printStackTrace();
        }


      }
